    public void SepeteEkle() {
        
    int selectedRow = UrunTablo1.getSelectedRow();
    if (selectedRow != -1) {
        String ad = (String) UrunTablo1.getValueAt(selectedRow, 1); // Ürün adı (1. sütun)
        int fiyat = (int) UrunTablo1.getValueAt(selectedRow, 2); // Fiyat (2. sütun)
        int urunId = (int) UrunTablo1.getValueAt(selectedRow, 0); // Ürün ID'si (0. sütun)
        
        boolean urunVar = false;
        
        DefaultTableModel sepetTabloModel = (DefaultTableModel) SepetTablo.getModel();
        for (int i = 0; i < SepetTablo.getRowCount(); i++) {
            int mevcutUrunId = (int) SepetTablo.getValueAt(i, 0); // Sepetteki ürünün ID'si
            if (mevcutUrunId == urunId) {
                int mevcutAdet = (int) SepetTablo.getValueAt(i, 2); // Mevcut adet
                sepetTabloModel.setValueAt(mevcutAdet + 1, i, 2); // Adet artışı
                urunVar = true;
                break;
            }
        }
        
        if (!urunVar) {

            sepetTabloModel.addRow(new Object[]{urunId, ad, 1, fiyat}); 
        }

        ToplamFiyat += fiyat;
        ToplamYazi.setText("Toplam Fiyat: " + ToplamFiyat + "TL");

        JOptionPane.showMessageDialog(null, ad + " sepete eklendi!");
        } else {
        JOptionPane.showMessageDialog(null, "Lütfen bir ürün seçin!");
        }
    }

    
