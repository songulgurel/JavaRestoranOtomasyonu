    // Kullanıcı verilerini veritabanına kaydetme
    private boolean saveUserData(String mail, String telNo, String adres, String kartNo, String cvv, String skt, String id) {
    try {
        // Veritabanı bağlantısı
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/restoran", "root", "");

        String query = "UPDATE kullanicilar SET mail = ?, telNo = ?, adres = ?, kartNo = ?, cvv = ?, skt = ? WHERE id = ?";
        PreparedStatement ps = connection.prepareStatement(query);
        telNo = telnogirisi.getText().replaceAll("[^0-9]", "");
        kartNo = kartnogirisi.getText().replaceAll("[^0-9]", "");
        cvv = cvvgirisi.getText().replaceAll("[^0-9]", "");
        skt = tarihgirisi.getText().replaceAll("[^0-9]", "");
        ps.setString(1, mail);
        ps.setString(2, telNo);
        ps.setString(3, adres);
        ps.setString(4, kartNo);
        ps.setString(5, cvv);
        ps.setString(6, skt);
        ps.setString(7, id); 

        int rowsAffected = ps.executeUpdate();
        System.out.println("Güncellenen satır sayısı: " + rowsAffected);
        return rowsAffected > 0;
        
    } catch (SQLException ex) {
        ex.printStackTrace(); 
        JOptionPane.showMessageDialog(null, "Veritabanı hatası: " + ex.getMessage());  
        return false;  
    }
}

    
