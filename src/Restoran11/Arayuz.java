    private void loadUserProfile(String Mail) {
    try {
        // Veritabanı bağlantısı
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/restoran", "root", "");
        String query = "SELECT * FROM kullanicilar WHERE mail = ?";
        PreparedStatement ps = connection.prepareStatement(query);
        ps.setString(1, Mail);
        ResultSet rs = ps.executeQuery();
        
        if (rs.next()) {
            adgirisi.setText(rs.getString("ad"));
            soyadgirisi.setText(rs.getString("soyad"));
            mailgirisi.setText(rs.getString("mail"));
            telnogirisi.setText(rs.getString("telNo"));
            adresgirisi.setText(rs.getString("adres"));
            kartnogirisi.setText(rs.getString("kartNo"));
            cvvgirisi.setText(rs.getString("cvv"));
            tarihgirisi.setText(rs.getString("skt"));
            idgirisi.setText(rs.getString("id"));
        } else {
            JOptionPane.showMessageDialog(null, "Kullanıcı Bulunamadı.");
        }
    } catch (SQLException ex) {
        ex.printStackTrace();  // Hata mesajını yazdır
        JOptionPane.showMessageDialog(null, "Kullanıcı Yükleme Hatası: " + ex.getMessage());
    }
}

